/**
 * Fetch the skills manifest and return the skill object for the given slug, enriched with metadata.
 * Returns null if the slug is not found.
 */
async function loadSkillData(slug) {
    let manifest;
    try {
        const res = await fetch('/api/skills-manifest');
        if (!res.ok) throw new Error('Manifest fetch failed: ' + res.status);
        manifest = await res.json();
    } catch (err) {
        console.error('[skills-data] Could not load manifest:', err);
        return null;
    }

    // Search all categories for the matching slug
    for (const category of manifest.categories) {
        const skill = category.skills.find(s => s.slug === slug);
        if (!skill) continue;

        // Retrieve metadata from our mapping
        const meta = REAL_ESTATE_SKILLS_METADATA[slug] || {};

        // Build file entries — filter out sub-directories, SKILL.md always first
        const files = (skill.files || [])
            .filter(f => !f.endsWith('/') && f !== '')
            .sort((a, b) => {
                if (a === 'SKILL.md') return -1;
                if (b === 'SKILL.md') return 1;
                return 0;
            })
            .map(f => ({
                name: f,
                // URL-encode each segment individually
                path: `/skills/${encodeURIComponent(category.name)}/${encodeURIComponent(skill.slug)}/${encodeURIComponent(f)}`,
                required: f === 'SKILL.md'  // SKILL.md is always required; everything else is optional
            }));

        const tierVal = meta.tier || 19;
        const isFree = (tierVal === 19); // Top 50 ($19 tier) are free preview

        return {
            name: meta.name || skill.name,
            tagline: `${meta.persona || 'AGENT'} · Tier $${tierVal} · ${meta.lever || 'Operations'}`,
            tags: [meta.persona || 'BUYER & SELLER', meta.lever || 'Revenue ↑'],
            description: meta.desc || 'Real Estate AI skill to optimize agent operations.',
            free: isFree,
            category: category.name.replace(/^\d+[\s._-]*/, ''), // Strip sorting digits prefix (e.g. "01. ")
            files,
            platforms: ['Claude', 'Claude Code', 'OpenAI Codex', 'OpenRouter', 'Cursor', 'Windsurf']
        };
    }

    return null; // slug not found
}
